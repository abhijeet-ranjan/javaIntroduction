import java.util.Scanner;
public class Main {
	
	public static void main(String[] args) {
    	Scanner scan = new Scanner(System.in);
		int n = scan.nextInt();
		scan.close();
		int i = 1 ;
		while(i <= n){
			int space = i-1;
			while(space > 0 ){
				System.out.print(" ");
				space--;
			}
			int star = n;
			while(star > 0){
				System.out.print("*");
				star--;
			}
			System.out.println();
			i++;
		}

	}
}
